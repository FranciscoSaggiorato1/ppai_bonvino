class Usuario:
    #Propiedades de la clase Usuario
    contraseña = ""
    nombre = ""
    premium = None

def __init__(self, nombre, contraseña, premium):
    self.nombre = nombre
    self.contraseña = contraseña
    self.premium = premium
def new(self, nombre, contraseña, premium):
    return Usuario(nombre, contraseña, premium)
#Metodos de la clase Usuario
def getNombre(self, nombre):
    return self.nombre
def setNombre(self, nombre):
    self.nombre = nombre
def getContraseña(self, contraseña):
    return self.contraseña
def setContraseña(self, contraseña):
    self.contraseña = contraseña
def getPremium(self):
    return self.premium
def setPremium(self, premium):
    self.premium = premium
