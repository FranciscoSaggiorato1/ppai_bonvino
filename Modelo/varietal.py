class Varietal():
    id=""
    descripcion = ""
    porcentajeComposicion = 0
    tipoUva= None
def __init__(self,id, descripcion, porcentajeComposicion):
    self.id = id
    self.descripcion = descripcion
    self.porcentajeComposicion = porcentajeComposicion
def new(self,id, descripcion, porcentajeComposicion):
    return Varietal(id,descripcion, porcentajeComposicion)  

def get_Id(self):
    return self.id
def set_Id(self,id):
    self.id = id 
def get_Descripcion(self):
    return self.descripcion
def set_Descripcion(self,descripcion):
    self.descripcion = descripcion
def get_Porcentaje_Composicion(self):
    return self.porcentajeComposicion
def set_Porcentaje_Composicion(self,porcentajeComposicion):
    self.porcentajeComposicion = porcentajeComposicion
def get_Tipo_Uva(self):
    return self.tipoUva
def set_Tipo_Uva(self,tipoUva):
    self.tipoUva = tipoUva

     